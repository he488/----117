#include "bsp_beep.h"

/**************************************************************************
函数功能：蜂鸣器GPIO初始化
入口参数：无
返回  值：无
说    明：将PB5配置为推挽输出，默认输出高电平（蜂鸣器不响）
**************************************************************************/
void Beep_GPIO_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;

    // 使能GPIOB时钟
    RCC_APB2PeriphClockCmd(BEEP_CLK, ENABLE);

    // 配置PB5为推挽输出，50MHz
    GPIO_InitStructure.GPIO_Pin = BEEP_PIN;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(BEEP_PORT, &GPIO_InitStructure);

    // 初始化时默认关闭蜂鸣器（输出高电平）
    BEEP_OFF();
}

/**************************************************************************
函数功能：蜂鸣器响一声
入口参数：无
返回  值：无
说    明：蜂鸣器响约200ms后自动关闭
**************************************************************************/
void Beep_Once(void)
{
    BEEP_ON();              // 打开蜂鸣器
    delay_ms(200);          // 持续200ms
    BEEP_OFF();             // 关闭蜂鸣器
}

/**************************************************************************
函数功能：蜂鸣器响指定时长
入口参数：ms  — 响铃时长，单位毫秒
返回  值：无
说    明：蜂鸣器响指定毫秒数后自动关闭
**************************************************************************/
void Beep_On_Time(uint16_t ms)
{
    BEEP_ON();              // 打开蜂鸣器
    delay_ms(ms);           // 持续指定时长
    BEEP_OFF();             // 关闭蜂鸣器
}

/**************************************************************************
函数功能：蜂鸣器响N次
入口参数：n  — 响铃次数
返回  值：无
说    明：每次响200ms，间隔100ms，响完N次后关闭
**************************************************************************/
void Beep_N_Times(uint8_t n)
{
    uint8_t i;
    for (i = 0; i < n; i++)
    {
        BEEP_ON();          // 打开蜂鸣器
        delay_ms(200);      // 持续200ms
        BEEP_OFF();         // 关闭蜂鸣器
        if (i < n - 1)      // 最后一次不延时
        {
            delay_ms(100);  // 间隔100ms
        }
    }
}